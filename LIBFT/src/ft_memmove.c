#include "../include/LIBFT.h"

static	void	memmove_schandle(size_t i, size_t n, char *d, char *s)
{
	if (d < s)
	{
		i = 0;
		while (i < n)
		{
			d[i] = s[i];
			i++;
		}
	}
	else
	{
		i = n;
		while (i > 0)
		{
			i--;
			d[i] = s[i];
		}
	}
}

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	char	*d;
	char	*s;
	size_t	i;

	i = 0;
	d = (char *)dest;
	s = (char *)src;
	if (d == s)
	{
		return (dest);
	}
	memmove_schandle(i, n, d, s);
	return (dest);
}
